package Graph;

public class Stackx {
	
	private final int SIZE= 20;
	private int[] st;
	private int top; 
	
	//--------------------------------------------------------------------------------------------------------------------------------------------------
	public Stackx() 
	{	
		st = new int[SIZE]; // make array 
		top = -1;
	}
	//---------------------------------------------------------------------------------------------------------------------------------------------
	/**
	 * put item on stack
	 * @param j
	 */
	public void push(int j)  
	{
		st[++top] = j;
	}
	//--------------------------------------------------------------------------------------------------------------------------------------------------
	/**
	 * take item off stack
	 */
	public int pop()
	{
		return st[top--];
	}
	//--------------------------------------------------------------------------------------------------------------------------------------------------
	/**
	 * peek at item of stack 
	 * 
	 */
	public int peek()
	{
		return st[top];
	}
	//--------------------------------------------------------------------------------------------------------------------------------------------------
	/**
	 * true if nothing on stack
	 * @return
	 */
	public boolean isEmpty()
	{
		return (top ==-1);
	}
	//--------------------------------------------------------------------------------------------------------------------------------------------------
	//end class
}
